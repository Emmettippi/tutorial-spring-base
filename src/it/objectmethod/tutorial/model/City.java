package it.objectmethod.tutorial.model;

public class City {

	private Long idcity;
	private String code;
	private String name;

	public Long getIdcity() {
		return idcity;
	}

	public void setIdcity(Long idcity) {
		this.idcity = idcity;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
		


